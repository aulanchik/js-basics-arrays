'use strict';

// Array.prototype.includes()
// The includes() method determines whether an array includes a certain element,
// returning true or false as appropriate.

const fruits = ['apple', 'banana', 'orange', 'kiwi', 'pineapple'];

console.log('Array.prototype.includes():');
console.log('Words: ', fruits);
console.log('Includes "apple": ', fruits.includes('apple'));
console.log('Includes "avocado": ', fruits.includes('avocado'));
